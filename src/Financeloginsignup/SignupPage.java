        String accname = jTextField2.getText();
            String email = jTextField1.getText();
            String password1 = new String(jPasswordField1.getPassword());
            BigInteger startingbalance = new BigInteger(jTextField3.getText());
            
            System.out.println(accname);
            System.out.println(password1);
            System.out.println(email);
            System.out.println(startingbalance);
        
//        inserting into db
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/financemanagement","root","1234");
            
            String query = "insert into userdetailsfinance values('"+accname+"','"+password1+"','"+email+"','"+startingbalance+"')" ;
            Statement st = con.createStatement();
            int rs = st.executeUpdate(query);
            
            String sql1 = "CREATE TABLE "+accname+"(accname varchar(25),startingbal bigint,expdes varchar(255),expamt bigint,expcat varchar(25),incdes varchar(25),incamt bigint,inccat varchar(25),balance bigint,findate date,month bigint,year bigint)";
            
            st.executeUpdate(sql1);
            System.out.println("Created the table for the user");
            
            
//            int rs = 0;
            if(rs>0){
                   JOptionPane.showMessageDialog(null, "Your account has been sucessfully created", "Signup Sucessfull", JOptionPane.PLAIN_MESSAGE, null);
                   LoginPage lp = new LoginPage();
                    lp.setVisible(true);
                    this.dispose();
                }
            else{
                    System.out.print("came her even after fail");
                    JOptionPane.showMessageDialog(null, "Cannot Signup try sometimes later", "Signup Error", JOptionPane.ERROR_MESSAGE, null);
                    
            }
            
            }
            catch (Exception e) {
                System.out.println(e);
                System.out.println("not connected");
                JOptionPane.showMessageDialog(null, "This DB is for locaolhost pls enable it and run", "Authentication Error", JOptionPane.ERROR_MESSAGE, null);
                
            }
